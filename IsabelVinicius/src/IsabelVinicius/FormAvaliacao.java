    private void preencherTabela() {
        //Vai pegar a lista de produtos no "this.venda" e inserir no tblProdutos
        List<Nota> listaAvaliacao = this.avaliacao.getListaNota();

        DefaultTableModel tabela = (DefaultTableModel) tblAvaliacao.getModel();

        //zera as linhas da tabela para evitar valores repetidos
        tabela.setNumRows(0);

        for (Nota n : listaAvaliacao) {
            Object[] novoProd = new Object[]{
                n.getProntuario(),
                n.getNome(),
                n.getNota()
            };
            tabela.addRow(novoProd);
        }
        txtMediaTotal.setText(String.valueOf(this.avaliacao.getMediaTotal()));
    }

    private void preencherTabelaAvaliacao() {
        List<Avaliacao> listaAvaliacao = this.ca.getAvaliacao();
        DefaultTableModel tabelaAvaliacao = (DefaultTableModel) tblNotas.getModel();

        tabelaAvaliacao.setNumRows(0);

        for (Avaliacao a : listaAvaliacao) {
            Object[] novaLinha = new Object[]{
                a.getDataAvaliacao(),
                a.getAvaliacao(),
                a.getformaAvaliacao(),
                a.getMediaTotal()
            };
            tabelaAvaliacao.addRow(novaLinha);
        }

        txtTotalMedia.setText(String.valueOf(this.ca.getNotaTotalAvaliacao()));
    }

