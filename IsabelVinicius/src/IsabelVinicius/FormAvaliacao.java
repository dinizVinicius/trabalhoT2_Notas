    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        this.avaliacao = new Avaliacao();
        this.avaliacao.setdataAvaliacao(txtData.getText());
        this.avaliacao.setformaAvaliacao(cmbFormaAvaliacao.getSelectedItem().toString());
        this.habilitarNota(true);
        this.habilitarAvaliacao(false);
    }//GEN-LAST:event_btnIniciarActionPerformed

    private void cmbFormaAvaliacaoFiltroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbFormaAvaliacaoFiltroActionPerformed
        String formaAvaliacao = cmbFormaAvaliacaoFiltro.getSelectedItem().toString();

        if (!formaAvaliacao.equals("Escolha...")) {
            double mediaTotal = this.ca.getMediaTotalPorFormaAvaliacao(formaAvaliacao);
            JOptionPane.showMessageDialog(this, "A media total na " + formaAvaliacao + ": " + mediaTotal);
        }

    }//GEN-LAST:event_cmbFormaAvaliacaoFiltroActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        //finalizar a venda (add ela no controlaVendas)
        this.ca.adicionar(this.avaliacao);

        //limpar os campos do form e o tblProdutos
        txtProntuario.setText("");
        txtData.setText("");
        txtNota.setText("");
        txtNome.setText("");
        txtMediaTotal.setText("0.00");
        DefaultTableModel tabelaProdutos = (DefaultTableModel) tblAvaliacao.getModel();
        tabelaProdutos.setNumRows(0);
        //desabilitar a parte dos produtos e habilitar a parte da venda
        habilitarNota(false);
        habilitarAvaliacao(true);

        JOptionPane.showMessageDialog(this, "Cadastro de notas finalizado com sucesso");

        //atualizar a tabela de vendas
        preencherTabelaAvaliacao();
    }//GEN-LAST:event_btnFinalizarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        Nota n = new Nota();
        n.setNome((txtNome.getText()));
        n.setNota(Double.parseDouble(txtNota.getText()));
        n.setProntuario(txtProntuario.getText());

        this.avaliacao.addAvaliacao(n);
        preencherTabela();

        txtProntuario.setText("");
        txtNota.setText("");
        txtNome.setText("");

// TODO add your handling code here:
    }//GEN-LAST:event_btnAdicionarActionPerformed

